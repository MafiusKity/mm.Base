#This function expects that m.base1 and m.base2 are matrices that form a base in
#R^n spaces, and vb1 a vector described in m.base1;
#The matrices m.base1 and m.base2 shall contain the constituent vectors 
#of the base cobinated in columns;
#This function return vb1 described in m.base2;
#This function works only quadratic matrices;

mm.Base<-function(m.base1,m.base2,vb1){
  if((is.matrix(m.base1)||is.matrix(m.base2))!=T){
    stop("This fuction expects that m.base1 and m.base are matrices\n")
  }
  m.m1Tom2=solve(m.base1,m.base2)
  vb2=solve(m.m1Tom2,vb1)
  return(vb2)
}

